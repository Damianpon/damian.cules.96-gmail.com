# 2. Pole koła
r = 7
pii = 3.14
poleKola = pii * (r ** 2)
print("Pole koła równa się: ", poleKola)
# 3. Pole trapezu
a = 9
b = 3
h = 6.5
pole = ((a + b) / 2) * h
print("Pole trapezu równa się", pole)
# 4 Obj kuli
pii = 3.14
r = 7 / 8
objetosc = (4 / 3) * pii * (r ** 3)
